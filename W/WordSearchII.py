'''
-Hard-
*Backtracking*
*Trie*


Given an m x n board of characters and a list of strings words, return all words on the board.

Each word must be constructed from letters of sequentially adjacent cells, where adjacent cells are horizontally or vertically neighboring. The same letter cell may not be used more than once in a word.

 

Example 1:


Input: board = [["o","a","a","n"],["e","t","a","e"],["i","h","k","r"],["i","f","l","v"]], 
words = ["oath","pea","eat","rain"]
Output: ["eat","oath"]
Example 2:


Input: board = [["a","b"],["c","d"]], words = ["abcb"]
Output: []
 

Constraints:

m == board.length
n == board[i].length
1 <= m, n <= 12
board[i][j] is a lowercase English letter.
1 <= words.length <= 3 * 10^4
1 <= words[i].length <= 10
words[i] consists of lowercase English letters.
All the strings of words are unique.

'''

from collections import defaultdict

class Node(defaultdict):
    def __init__(self):
        super().__init__(Node)
        self.terminal = False        


class Trie(object):

    def __init__(self):
        """
        Initialize your data structure here.
        """
        self.root = Node()
        

    def insert(self, word):
        """
        Inserts a word into the trie.
        :type word: str
        :rtype: None
        """
        cur = self.root
        for c in word:    
            cur = cur[c]
        cur.terminal = True
        


        

    def search(self, word):
        """
        Returns if the word is in the trie.
        :type word: str
        :rtype: bool
        """
        cur = self.root
        for c in word:
            if c not in cur:
                return False
            cur = cur[c]
        return cur.terminal

        

    def startsWith(self, prefix):
        """
        Returns if there is any word in the trie that starts with the given prefix.
        :type prefix: str
        :rtype: bool
        """
        cur = self.root
        for c in prefix:
            if c not in cur:
                return False
            cur = cur[c]
        return True

class Solution(object):
    def findWords(self, board, words):
        """
        :type board: List[List[str]]
        :type words: List[str]
        :rtype: List[str]
        """
        m, n = len(board), len(board[0])
        trie = Trie()
        for w in words: trie.insert(w)
        res = set()
        def dfs(i,j,visited,cur,path):
            if cur.terminal: res.add(path)
            for dx, dy in [(0,-1), (0,1), (-1,0), (1,0)]:
                x, y = i+dx, j+dy
                if 0<=x<m and 0<=y<n and not visited[x][y] and board[x][y] in cur:
                    visited[x][y] = True                    
                    dfs(x,y,visited,cur[board[x][y]], path+board[x][y])
                    visited[x][y] = False

        visited = [[False]*n for _ in range(m)]
        for i in range(m):
            for j in range(n):
                if board[i][j] in trie.root:
                    visited[i][j] = True
                    dfs(i, j, visited, trie.root[board[i][j]], board[i][j])
                    visited[i][j] = False
        return list(res)


